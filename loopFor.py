# Estrutura de repetição <for> 

lista = ['cão', 'gato', 'coelho']

for indice in lista:
  print(indice)


palavra = 'algoritmos'

for letra in palavra:
  if letra in 'aiueo':
    print(letra)


for n in range(10):
  print(n)


for x in range(1, 11):
  print(x)

for y in range(2, 20, 2):
  print(y)

